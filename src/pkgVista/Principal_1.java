     private void obtenertitulos() throws SQLException {
        dbConexion cn = new dbConexion();
        Connection conn = cn.getConexion();
        ResultSet rs;
        String sql = "SELECT idleccion , tituloteoria FROM leccion";
        PreparedStatement pstm = conn.prepareStatement(sql);
        rs = pstm.executeQuery();

        while (rs.next()) {
            clsLeccion usu = new clsLeccion();
            usu.setId(rs.getInt("idleccion"));
            usu.setTituloteoria(rs.getString("tituloteoria"));
            cboTitulos.addItem(usu);
System.out.println("este es hmtl  "+usu);
        }

    } 
     private void obtenerjs() throws SQLException {
        
        dbLeccion_js usu = new dbLeccion_js() ;
        clsLeccion_js[] titulo  = usu.getTituloLeccion();
        int limite = titulo.length ;
        for (int js = 0; js < limite; js++) {
             if (titulo!=null) {
             cboTitulosjs.addItem(titulo[js].getTituloteoria());
             
         }
         }
         
        
    }   
