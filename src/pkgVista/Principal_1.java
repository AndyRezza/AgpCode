    static String quitarsalto (String cadena){
        String[] retornosplit = cadena.split("\r\n");
        String retorno = "" ; 
        for (int i = 0; i < retornosplit.length; i++) {
            retorno += retornosplit[i] ;
        }
        return retorno;
    }
    
